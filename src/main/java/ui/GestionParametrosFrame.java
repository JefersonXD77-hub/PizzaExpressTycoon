    private void inicializarVista() {
        configurarTabla();
        cargarDatosAdmin();
        limpiarFormulario();
        cargarParametros();
    }

    private void configurarTabla() {
        modeloTabla = new DefaultTableModel(
                new Object[]{"Nivel", "Tiempo Base"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tblParametros.setModel(modeloTabla);
    }

    private void cargarDatosAdmin() {
        lblTitulo.setText("Super Admin - Parámetros del Juego");
        lblAdmin.setText("Usuario: " + sesion.getNickname());
        lblEstado.setText("Estado: listo");
    }

    private void cargarParametros() {
        modeloTabla.setRowCount(0);

        List<ParametroJuego> parametros = parametroJuegoDAO.listarTodos();

        for (ParametroJuego parametro : parametros) {
            modeloTabla.addRow(new Object[]{
                parametro.getNivel(),
                parametro.getTiempoBase()
            });
        }
    }

    private void limpiarFormulario() {
        nivelSeleccionado = null;
        txtNivel.setText("");
        txtTiempoBase.setText("");
    }

    private int construirTiempoBaseDesdeFormulario() {
        if (nivelSeleccionado == null) {
            throw new RuntimeException("Selecciona un nivel.");
        }

        String tiempoTexto = txtTiempoBase.getText().trim();

        if (tiempoTexto.isEmpty()) {
            throw new RuntimeException("Ingresa el tiempo base.");
        }

        int tiempoBase;
        try {
            tiempoBase = Integer.parseInt(tiempoTexto);
        } catch (NumberFormatException e) {
            throw new RuntimeException("El tiempo base debe ser un número entero.");
        }

        if (tiempoBase <= 0) {
            throw new RuntimeException("El tiempo base debe ser mayor que 0.");
        }

        return tiempoBase;
    }

