    private void tblParametrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblParametrosMouseClicked

        int fila = tblParametros.getSelectedRow();
        if (fila == -1) {
            return;
        }

        nivelSeleccionado = Integer.valueOf(tblParametros.getValueAt(fila, 0).toString());
        txtNivel.setText(tblParametros.getValueAt(fila, 0).toString());
        txtTiempoBase.setText(tblParametros.getValueAt(fila, 1).toString());

    }//GEN-LAST:event_tblParametrosMouseClicked

