    private void btnActualizarParametroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarParametroActionPerformed

        try {
            int tiempoBase = construirTiempoBaseDesdeFormulario();

            boolean ok = parametroJuegoDAO.actualizarTiempoBase(nivelSeleccionado, tiempoBase);

            if (!ok) {
                throw new RuntimeException("No se pudo actualizar el parámetro.");
            }

            cargarParametros();
            lblEstado.setText("Estado: nivel " + nivelSeleccionado + " actualizado");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnActualizarParametroActionPerformed

